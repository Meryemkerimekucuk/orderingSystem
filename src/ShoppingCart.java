private void updateTable() {
    DefaultTableModel model = (DefaultTableModel) tblTotal.getModel();
    model.setRowCount(0); 
    for (Object[] satir : recordedData) {
        model.addRow(satir);
    }
}
    public void sendData(List<Object[]> veriler) {
        DefaultTableModel model = (DefaultTableModel) tblTotal.getModel();
        model.setRowCount(0); 
        for (Object[] satir : veriler) {
            model.addRow(satir);
        }
        recordedData.addAll(veriler);
        updateTable();
    }
    

