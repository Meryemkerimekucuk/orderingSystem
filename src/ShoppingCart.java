    private void btnTotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTotalActionPerformed
        double total = 0;
DefaultTableModel model = (DefaultTableModel) tblTotal.getModel();
int rowCount = model.getRowCount();

for (int i = 0; i < rowCount; i++) {
    try {
        Object wageData = model.getValueAt(i, 6); 
        double price = Double.parseDouble(wageData.toString());
        total += price;
    } catch (NumberFormatException e) {
        
        System.err.println("Failed " + model.getValueAt(i, 6));
    }
}
        txtTotalAmount.setText(String.valueOf(total));
    }//GEN-LAST:event_btnTotalActionPerformed

